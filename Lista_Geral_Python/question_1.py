# Escreva um programa que leia três 
# números inteiros e calcule a sua média aritmética

nota_1 = float(input("Informe a primeira nota: "))
nota_2 = float(input("Informe a segundo nota: "))
nota_3 = float(input("Informe a terceira nota: "))

print(f"As notas informadas foram: {nota_1}, {nota_2} e {nota_3}. A média delas é {(nota_1 + nota_2 + nota_3)/3:.1f}")